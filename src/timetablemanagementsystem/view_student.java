    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        int count=0;
        
        Connection connection=getConnection();
        String studentQuery = "SELECT Count(Concat(AcademicYearAndSem, ProgrammeName, GroupNo, SubGroupNo)) FROM students WHERE AcademicYearAndSem='"+yearandsem.getSelectedItem()+"' and ProgrammeName='"+programme.getSelectedItem()+"' and GroupNo='"+groupno.getSelectedItem()+"' and SubGroupNo= '"+subgroupno.getSelectedItem()+"'";
        Statement st;
        ResultSet rs;
        
        try{
        st= connection.createStatement();
        rs= st.executeQuery(studentQuery);
        rs.next();
        count= rs.getInt(1);
        
        }catch(Exception e){
        e.printStackTrace();
        }
        
        if(count>0){
            
           JOptionPane.showMessageDialog(null,"Student Group already exists !!!");
        }
        else{
        String query=" UPDATE `students` SET `AcademicYearAndSem`='"+yearandsem.getSelectedItem()+"',`ProgrammeName`='"+programme.getSelectedItem()+"',`GroupNo`='"+groupno.getSelectedItem()+"',`SubGroupNo`='"+subgroupno.getSelectedItem()+"',`GroupId`='"+yearandsem.getSelectedItem()+"."+programme.getSelectedItem()+"."+groupno.getSelectedItem()+"',`SubGroupId`='"+yearandsem.getSelectedItem()+"."+programme.getSelectedItem()+"."+groupno.getSelectedItem()+"."+subgroupno.getSelectedItem()+"' WHERE `BatchID` ="+Id.getText();
        executeSQlQuery(query, "Updated");
        }
    }//GEN-LAST:event_EditActionPerformed

