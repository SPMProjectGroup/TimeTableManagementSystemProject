    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       String workingDays = jTextField_NoOfWorkingDays.getText();
       String workingHours = jTextField_WorkingHours.getText();
       String wdSaturday = jTextField_WdSaturday.getText();
       String wdSunday = jTextField_WdSunday.getText();
       
       DefaultCategoryDataset dataset = new DefaultCategoryDataset();
       dataset.setValue(new Double(workingDays), "Values", "NoOfWorkingDays");
       dataset.setValue(new Double(workingHours), "Values", "NoOfWorkingHours");
       dataset.setValue(new Double(wdSaturday), "Values", "SaturdayWorkingDays");
       dataset.setValue(new Double(wdSunday), "Values", "SundayWorkingDays");
       
       
       JFreeChart chart = ChartFactory.createBarChart3D("Statistics For Selected Lecturer", "Parameters", "Values", dataset, PlotOrientation.VERTICAL, rootPaneCheckingEnabled, rootPaneCheckingEnabled, rootPaneCheckingEnabled);
       chart.setBackgroundPaint(Color.LIGHT_GRAY);
       chart.getTitle().setPaint(Color.red);
       CategoryPlot p=chart.getCategoryPlot();
       p.setRangeGridlinePaint(Color.BLUE);
       ChartFrame frame= new ChartFrame("Bar chart for Lecturers", chart);
       frame.setVisible(true);
       frame.setSize(650, 550);
    }//GEN-LAST:event_jButton1ActionPerformed

