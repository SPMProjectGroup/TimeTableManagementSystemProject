     public ArrayList<Lecturer_1> getLecturersList()
    {
      ArrayList<Lecturer_1> lecturersList = new ArrayList<Lecturer_1>();
      Connection connection  = getConnection();
      
      String query = "SELECT * FROM `workingdays`";
      Statement st;
      ResultSet rs;
      
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         Lecturer_1 lecturer_1;
         while(rs.next())
         {
           lecturer_1 = new Lecturer_1(rs.getInt("TimetableID"), rs.getString("UserType"), rs.getInt("NoOfWorkingDays"), rs.getInt("WDMonday"), rs.getInt("WDTuesday"), rs.getInt("WDWednesday"), rs.getInt("WDThursday"), rs.getInt("WDFriday"), rs.getInt("WDSaturday"), rs.getInt("WDSunday"), rs.getInt("WorkingHours"));  
           lecturersList.add(lecturer_1);
           
         }
     } catch(Exception e){
        e.printStackTrace();
     }
     return lecturersList;
    }
    
    
