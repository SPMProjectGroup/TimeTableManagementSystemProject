    public Connection getConnection()
    {
        Connection con;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timetablemanagementsystem", "root", "");
            return con;
        }catch(Exception e){
            e.printStackTrace();
            return null;
        }   
    }
    
    
    public ArrayList<Location> getLocationsList()
    {
      ArrayList<Location> locationsList = new ArrayList<Location>();
      Connection connection  = getConnection();
      
      String query = "SELECT * FROM `location`";
      Statement st;
      ResultSet rs;
      
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         Location location;
         while(rs.next())
         {
           location = new Location(rs.getInt("LocationID"), rs.getString("BuildingName"), rs.getString("RoomName"),rs.getString("RoomType"));  
           locationsList.add(location);
           
         }
     } catch(Exception e){
        e.printStackTrace();
     }
     return locationsList;
    }
    
    
//Display location data in JTable
    
    public void Show_Locations_In_JTable(){
        ArrayList<Location> list = getLocationsList();
        DefaultTableModel model = (DefaultTableModel)jTable_DisplayLocations.getModel();
        Object[] row = new Object[4];
        for(int  i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getBuildingName();
            row[2] = list.get(i).getRoomName();
            row[3] = list.get(i).getRoomType();
            
            model.addRow(row);
            
        }
        
    }
    
    
    //Execute the sql query
    
    public void  executeSQlQuery(String query, String message)
    {
        Connection con = getConnection();
        Statement st;
        try{
            st = con.createStatement();
            if((st.executeUpdate(query)) == 1)
            {
                
                //refreash jtable data
                DefaultTableModel model = (DefaultTableModel)jTable_DisplayLocations.getModel();
                model.setRowCount(0);
                Show_Locations_In_JTable();
                
                JOptionPane.showMessageDialog(null, "Location Record "+message+" Successfully");
            }else{
                JOptionPane.showMessageDialog(null, "Location Record Not"+message);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
