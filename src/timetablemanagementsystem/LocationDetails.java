    private void jButton_InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_InsertActionPerformed
        
        //String query  = "INSERT INTO `location`(`BuildingName`, `RoomName`, `RoomType`) VALUES ('"+jTextField_BuildingName.getText()+"','"+jTextField_RoomName.getText()+"','"+jTextField_RoomType.getText()+"')";
        //executeSQlQuery(query,"Inserted");
        
        String buildingName = jTextField_BuildingName.getText();
        String roomName = jTextField_RoomName.getText();
        String roomType = jComboBox_RoomType.getSelectedItem().toString();
		       
        int roomNameCount = 0;
        
        if(buildingName.equals("")){
            //System.out.println("");
            JOptionPane.showMessageDialog(null, "Building Name is required !!!");   
        }else if(roomName.equals("")){
            JOptionPane.showMessageDialog(null, "Room Name is required !!!");   
        }else if(roomType.equals("")){
            JOptionPane.showMessageDialog(null, "Room Type is required !!!");   
        }else{
            
            
            Connection connection = getConnection();
            String queryRoomName = "SELECT COUNT(RoomName) from location WHERE RoomName = '"+roomName+"'";
            Statement statement;
            ResultSet res;

            try{
                statement = connection.createStatement();
                res = statement.executeQuery(queryRoomName);
                res.next();
                roomNameCount = res.getInt(1);
            }catch(Exception e){
                e.printStackTrace();
            }
        
            if(roomNameCount>0){
                 JOptionPane.showMessageDialog(null, roomName+ " room is already exist, Cannot add this room ");   
            }else{
                 
		String query  = "INSERT INTO location (BuildingName, RoomName, RoomType) VALUES ('"+buildingName+"','"+roomName+"','"+roomType+"')";
        executeSQlQuery(query,"Inserted");
		}
        
       
    }//GEN-LAST:event_jButton_InsertActionPerformed
    }
