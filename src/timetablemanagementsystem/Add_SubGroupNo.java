    //to retrieve data from the sub group number table
    public ArrayList<SubGroupNo> getSubGroupNoList(){
    
    ArrayList<SubGroupNo> SubGroupNoList= new ArrayList<SubGroupNo>(); 
    Connection connection=getConnection();
    
    String query= "SELECT * FROM `studentsubgroups` ";
    Statement st;
    ResultSet rs;
    
     try{
     st = connection.createStatement();
     rs= st.executeQuery(query);
     SubGroupNo subgroupNo;
     while(rs.next()){
     subgroupNo = new SubGroupNo(rs.getInt("SubGroupNoID"),rs.getInt("SubGroupNo"));
     SubGroupNoList.add(subgroupNo);
     }
     }catch(Exception e){
      e.printStackTrace();
     }
      return SubGroupNoList;  
    }
    
    //Display data in Jtable
    public void show_SubGroupNo(){
        
    ArrayList<SubGroupNo> list= getSubGroupNoList();
    DefaultTableModel model=(DefaultTableModel)SubGroupNo.getModel();
    Object[] row=new Object[2];
    
    for(int i=0; i<list.size(); i++){
        
        row[0]=list.get(i).getId();
        row[1]=list.get(i).getSubGroupNo();
        model.addRow(row);
    }
    
    }
    
    //Execute the sql query
    public void executeSQlQuery(String query,String message){
    
        Connection con= getConnection();
        Statement st;
        
        try{
        st= con.createStatement();
        if((st.executeUpdate(query))==1){
            
            //refresh Jtable data
            DefaultTableModel model=(DefaultTableModel) SubGroupNo.getModel();
            model.setRowCount(0);
            show_SubGroupNo();
           JOptionPane.showMessageDialog(null, message+ " Successfully");
        }else{
           JOptionPane.showMessageDialog(null,message+ "Can Not be done ");
        }
        }catch(Exception e){}
    
    }
