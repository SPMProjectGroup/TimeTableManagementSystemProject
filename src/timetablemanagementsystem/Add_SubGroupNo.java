    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        int count=0;
        String subGroupNo=SubGroupNumber.getText();
        Connection connection=getConnection();
        String gnoQuery = "SELECT Count(SubGroupNo) FROM studentsubgroups WHERE SubGroupNo='"+subGroupNo+"'";
        Statement st;
        ResultSet rs;
        
        try{
        st= connection.createStatement();
        rs= st.executeQuery(gnoQuery);
        rs.next();
        count= rs.getInt(1);
        
        }catch(Exception e){
        e.printStackTrace();
        }
        
        if(SubGroupNumber.getText().trim().isEmpty()){
          err.setText("Please enter a Sub Group Number to continue !!!");
        }
        else if(count>0){
            
           JOptionPane.showMessageDialog(null,"Sub Group number " +SubGroupNumber.getText()+" is already exists !!!");
        }
        else{
        String query = "INSERT INTO `studentsubgroups`(`SubGroupNo`) VALUES ('"+SubGroupNumber.getText()+"')";

        executeSQlQuery(query, "Sub Group Number Inserted");
        }
    }//GEN-LAST:event_AddActionPerformed

