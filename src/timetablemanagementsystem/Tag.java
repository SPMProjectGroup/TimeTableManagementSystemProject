    //to retreive the data in the tag table
    public ArrayList<TagName> getTagList(){
    
    ArrayList<TagName> TagList= new ArrayList<TagName>(); 
    Connection connection=getConnection();
    
    String query= "SELECT * FROM `tags` ";
    Statement st;
    ResultSet rs;
    
     try{
     st = connection.createStatement();
     rs= st.executeQuery(query);
     TagName tag;
     while(rs.next()){
     tag = new TagName(rs.getInt("TagID"),rs.getString("TagName"));
     TagList.add(tag);
     }
     }catch(Exception e){
      e.printStackTrace();
     }
      return TagList;  
    }
    
    //Display data in Jtable
    public void show_Tags(){
        
    ArrayList<TagName> list= getTagList();
    DefaultTableModel model=(DefaultTableModel)Tag.getModel();
    Object[] row=new Object[2];
    
    for(int i=0; i<list.size(); i++){
        
        row[0]=list.get(i).getId();
        row[1]=list.get(i).getTagname();
        model.addRow(row);
    }
    
    }
    
    //Execute the sql query
    public void executeSQlQuery(String query,String message){
    
        Connection con= getConnection();
        Statement st;
        
        try{
        st= con.createStatement();
        if((st.executeUpdate(query))==1){
            
            //refresh Jtable data
            DefaultTableModel model=(DefaultTableModel) Tag.getModel();
            model.setRowCount(0);
            show_Tags();
           JOptionPane.showMessageDialog(null, message+ " Successfully");
        }else{
           JOptionPane.showMessageDialog(null, message+" Can not be done ");
        }
        }catch(Exception e){}
    
    }
    
