    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        int count=0;
        String tag=TagName.getText();
        Connection connection=getConnection();
        String gnoQuery = "SELECT Count(TagName) FROM tags WHERE TagName='"+tag+"'";
        Statement st;
        ResultSet rs;
        
        try{
        st= connection.createStatement();
        rs= st.executeQuery(gnoQuery);
        rs.next();
        count= rs.getInt(1);
        
        }catch(Exception e){
        e.printStackTrace();
        }
        
        if(TagName.getText().trim().isEmpty()){
          err.setText("Please enter a Tag Name to continue !!!");
        }
        else if(count>0){
            
           JOptionPane.showMessageDialog(null,"Tag Name " +TagName.getText()+" is already exists !!!");
        }
        else{
        String query = "INSERT INTO `tags`(`TagName`) VALUES ('"+TagName.getText()+"')";
        
        executeSQlQuery(query, "Tag Name Inserted");
        }
    }//GEN-LAST:event_AddActionPerformed

