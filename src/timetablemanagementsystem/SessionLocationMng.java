    public ArrayList<SessionModel> getSessionList()
    {
      ArrayList<SessionModel> sessionList = new ArrayList<SessionModel>();
      Connection connection  = getConnection();
      
      String query = "SELECT * FROM `sessions`";
      Statement st;
      ResultSet rs;
      
     try{
         st = connection.createStatement();
         rs = st.executeQuery(query);
         SessionModel sessionModel;
         while(rs.next())
         {
           sessionModel = new SessionModel(rs.getInt("SessionId"), rs.getString("Tag"), rs.getString("StudentGroup"), rs.getString("Subject"), rs.getInt("NoOfStudents"), rs.getInt("sessionDuration"));  
           sessionList.add(sessionModel);
           
         }
     } catch(Exception e){
        e.printStackTrace();
     }
     return sessionList;
    }
    
    
