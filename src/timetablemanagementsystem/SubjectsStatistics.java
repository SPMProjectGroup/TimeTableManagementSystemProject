    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
       String lecHours = jTextField_NoOfLecHours.getText();
       String tuteHours = jTextField_NoOfTuteHours.getText();
       String labHours = jTextField_NoOfLabHours.getText();
       String evaluationHours = jTextField_NoOfEvaluationHours.getText();
       
       DefaultCategoryDataset dataset = new DefaultCategoryDataset();
       dataset.setValue(new Double(lecHours), "Values", "NoOfLecHours");
       dataset.setValue(new Double(tuteHours), "Values", "NoOfTuteHours");
       dataset.setValue(new Double(labHours), "Values", "NoOfLabHours");
       dataset.setValue(new Double(evaluationHours), "Values", "NoOfEvaluationHours");
       
       
       JFreeChart chart = ChartFactory.createBarChart3D("Statistics For Selected Subject", "Parameters", "Values", dataset, PlotOrientation.VERTICAL, rootPaneCheckingEnabled, rootPaneCheckingEnabled, rootPaneCheckingEnabled);
       chart.setBackgroundPaint(Color.LIGHT_GRAY);
       chart.getTitle().setPaint(Color.red);
       CategoryPlot p=chart.getCategoryPlot();
       p.setRangeGridlinePaint(Color.BLUE);
       ChartFrame frame= new ChartFrame("Bar chart for subjects", chart);
       frame.setVisible(true);
       frame.setSize(650, 550);
       
    }//GEN-LAST:event_jButton1ActionPerformed

