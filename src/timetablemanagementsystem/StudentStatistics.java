    public void Show_Students_In_JTable(){
        ArrayList<Student_1> list = getStudentsList();
        DefaultTableModel model = (DefaultTableModel)jTable_DisplayStudents.getModel();
        Object[] row = new Object[11];
        for(int  i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getStudentId();
            row[1] = list.get(i).getUserType();
            row[2] = list.get(i).getNoOfWorkingDays();
            row[3] = list.get(i).getWdMonday();
            row[4] = list.get(i).getWdTuesday();
            row[5] = list.get(i).getWdWednesday();
            row[6] = list.get(i).getWdThursday();
            row[7] = list.get(i).getWdFriday();
            row[8] = list.get(i).getWdSaturday();
            row[9] = list.get(i).getWdSunday();
            row[10] = list.get(i).getWorkingHours();
            
            model.addRow(row);
            
        }
        
    }
